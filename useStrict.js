//"use strict" es una palabra clave en JavaScript que se utiliza para activar el modo estricto de interpretación de código. 
// Al activar este modo, se busca mejorar la seguridad y la robustez del código al hacer que el intérprete de JavaScript sea más exigente en la forma en que se escribe el código.

// Un ejemplo de cómo utilizar "use strict" es el siguiente:

"use strict";
x = 10; 

// En este ejemplo, al activar el modo estricto con la declaración "use strict", se generaría un error al intentar asignar un valor a una variable "x" no declarada. Si no se utiliza "use strict", esta asignación simplemente se realizaría sin problemas.